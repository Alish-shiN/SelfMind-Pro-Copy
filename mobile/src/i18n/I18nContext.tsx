const translations: Record<AppLanguage, Record<keyof typeof en, string>> = {
  en,
  ru,
  kk,
};

const LanguageContext = createContext<LanguageContextValue | undefined>(
  undefined,
);

function normalizeLanguage(value: string | null): AppLanguage {
  return value === "ru" || value === "kk" ? value : "en";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>("en");
  const [ready, setReady] = useState(false);

  useEffect(() => {
    let cancelled = false;
    (async () => {
      const saved = await AsyncStorage.getItem(LANGUAGE_KEY);
      if (!cancelled) {
        setLanguageState(normalizeLanguage(saved));
        setReady(true);
      }
    })();
    return () => {
      cancelled = true;
    };
  }, []);

  const setLanguage = useCallback(async (next: AppLanguage) => {
    setLanguageState(next);
    await AsyncStorage.setItem(LANGUAGE_KEY, next);
  }, []);

  const t = useCallback(
    (key: string, params?: Record<string, string | number>) => {
      const dictionary = translations[language as AppLanguage] as Record<
        string,
        string
      >;
      const fallback = translations.en as Record<string, string>;
      const template = dictionary[key] || fallback[key] || key;
      if (!params) return template;
      return Object.entries(params).reduce(
        (text, [paramKey, value]) =>
          text.replace(new RegExp(`{{${paramKey}}}`, "g"), String(value)),
        template,
      );
    },
    [language],
  );

  const value = useMemo(
    () => ({ language, ready, setLanguage, t }),
    [language, ready, setLanguage, t],
  );

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useTranslation() {
  const context = useContext(LanguageContext);
  if (!context)
    throw new Error("useTranslation must be used within LanguageProvider");
  return context;
}
